def _flip_bit(bit):
    """翻转爻: 阳变阴, 阴变阳"""
    return 0 if bit == 1 else 1


def _generate_palace_hexagrams(palace_trigram_name):
    """
    根据宫主经卦生成本宫8个重卦。
    返回列表 [(上卦binary, 下卦binary, 宫序号), ...]
    """
    base = list(BA_GUA[palace_trigram_name]["binary"] * 2)  # 6爻: 下3 + 上3
    results = []

    # 本宫卦 (纯卦)
    current = list(base)
    results.append(tuple(current))

    # 一世到五世: 从初爻开始逐爻变
    for i in range(5):
        current[i] = _flip_bit(current[i])
        results.append(tuple(current))

    # 游魂卦: 从五世卦, 第4爻变回原来
    you_hun = list(current)
    you_hun[3] = base[3]
    results.append(tuple(you_hun))

    # 归魂卦: 下卦恢复本宫, 上卦保持游魂的上卦
    gui_hun = list(base[:3]) + list(you_hun[3:])
    results.append(tuple(gui_hun))

    return results


def _build_hexagram_table():
    """
    构建64卦查询表。
    返回: {(上卦名, 下卦名): {"name": 卦名, "palace": 宫名, "palace_order": 序号}}
    以及: {卦名: {"upper": 上卦名, "lower": 下卦名, "palace": 宫名, "palace_order": 序号}}
    """
    # 64卦名称表 - 按八宫顺序排列
    # 每宫8卦: 纯卦, 一世, 二世, 三世, 四世, 五世, 游魂, 归魂
    gua_names = {
        "乾": ["乾为天", "天风姤", "天山遁", "天地否", "风地观", "山地剥", "火地晋", "火天大有"],
        "兑": ["兑为泽", "泽水困", "泽地萃", "泽山咸", "水山蹇", "地山谦", "雷山小过", "雷泽归妹"],
        "离": ["离为火", "火山旅", "火风鼎", "火水未济", "山水蒙", "风水涣", "天水讼", "天火同人"],
        "震": ["震为雷", "雷地豫", "雷水解", "雷风恒", "地风升", "水风井", "泽风大过", "泽雷随"],
        "巽": ["巽为风", "风天小畜", "风火家人", "风雷益", "天雷无妄", "火雷噬嗑", "山雷颐", "山风蛊"],
        "坎": ["坎为水", "水泽节", "水雷屯", "水火既济", "泽火革", "雷火丰", "地火明夷", "地水师"],
        "艮": ["艮为山", "山火贲", "山天大畜", "山泽损", "火泽睽", "天泽履", "风泽中孚", "风山渐"],
        "坤": ["坤为地", "地雷复", "地泽临", "地天泰", "雷天大壮", "泽天夬", "水天需", "水地比"],
    }

    by_trigrams = {}
    by_name = {}

    for palace_name in PALACE_NAMES:
        hexagrams = _generate_palace_hexagrams(palace_name)
        names = gua_names[palace_name]

        for order, (lines, name) in enumerate(zip(hexagrams, names)):
            lower = BINARY_TO_GUA[(lines[0], lines[1], lines[2])]
            upper = BINARY_TO_GUA[(lines[3], lines[4], lines[5])]
            entry = {
                "name": name,
                "palace": palace_name,
                "palace_order": order,
                "upper": upper,
                "lower": lower,
            }
            by_trigrams[(upper, lower)] = entry
            by_name[name] = entry

    return by_trigrams, by_name


