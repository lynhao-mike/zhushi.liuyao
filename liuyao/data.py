# =============================================================================
# 旬空 Xun Kong (Void/Empty Branches)
# =============================================================================
# 六十甲子分为6旬, 每旬10天(天干一轮), 剩余2个地支为旬空

def get_xun_kong(day_gan, day_zhi):
    """
    根据日柱的天干地支计算旬空。
    返回两个空亡地支的元组。
    """
    gan_idx = TIAN_GAN.index(day_gan)
    zhi_idx = DI_ZHI.index(day_zhi)

    # 本旬起始地支 = 当前地支 - 当前天干序号 (mod 12)
    start_zhi = (zhi_idx - gan_idx) % 12

    # 旬空为本旬未覆盖的两个地支 (第10, 11位)
    kong1 = DI_ZHI[(start_zhi + 10) % 12]
    kong2 = DI_ZHI[(start_zhi + 11) % 12]

    return (kong1, kong2)


