# 从三合局引申：
#   巳酉丑 → 巳酉半合、酉丑半合、丑巳半合
#   亥卯未 → 亥卯半合、卯未半合、未亥半合
#   申子辰 → 申子半合、子辰半合、辰申半合
#   寅午戌 → 寅午半合、午戌半合、戌寅半合

BAN_HE_PAIRS = set()
for _wx, _members in SAN_HE.items():
    m = list(_members)
    BAN_HE_PAIRS.add((m[0], m[1]))
    BAN_HE_PAIRS.add((m[1], m[0]))
    BAN_HE_PAIRS.add((m[1], m[2]))
    BAN_HE_PAIRS.add((m[2], m[1]))
    BAN_HE_PAIRS.add((m[0], m[2]))
    BAN_HE_PAIRS.add((m[2], m[0]))


def is_ban_he(zhi_a, zhi_b):
    """判断两个地支是否构成半合"""
    return (zhi_a, zhi_b) in BAN_HE_PAIRS


