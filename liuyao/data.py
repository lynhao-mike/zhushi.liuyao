# =============================================================================
# 六亲 Liu Qin (Six Relations)
# =============================================================================
# 根据本宫五行与爻的五行关系确定六亲:
# 同我者 = 兄弟, 我生者 = 子孙, 生我者 = 父母, 我克者 = 妻财, 克我者 = 官鬼

def get_liu_qin(palace_wx, line_wx):
    """
    根据本宫五行和爻的五行,计算六亲关系。
    palace_wx: 本宫五行 (如 "金")
    line_wx: 爻的地支所属五行 (如 "水")
    """
    if palace_wx == line_wx:
        return "兄弟"
    elif WU_XING_SHENG[palace_wx] == line_wx:
        return "子孙"
    elif WU_XING_SHENG[line_wx] == palace_wx:
        return "父母"
    elif WU_XING_KE[palace_wx] == line_wx:
        return "妻财"
    elif WU_XING_KE[line_wx] == palace_wx:
        return "官鬼"
    else:
        return "未知"


