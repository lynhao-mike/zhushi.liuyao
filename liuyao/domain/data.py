# (区别于通行版 寅巳申/丑戌未/子卯/辰午酉亥)

SAN_XING_GROUPS = [
    ("寅", "巳", "申"),  # 三互刑
    ("午", "亥", "酉"),  # 三互刑
]
TU_XING = ("丑", "辰", "未", "戌")  # 四土互刑及自刑
ZI_MAO_XING = ("子", "卯")  # 子卯互刑


def get_xing_relations(line_zhi):
    """获取与指定地支相刑的所有地支."""
    relations = []
    for group in SAN_XING_GROUPS:
        if line_zhi in group:
            for z in group:
                if z != line_zhi:
                    relations.append(z)
    if line_zhi in TU_XING:
        for z in TU_XING:
            if z != line_zhi:
                relations.append(z)
        relations.append(line_zhi)  # 自刑
    if line_zhi in ZI_MAO_XING:
        for z in ZI_MAO_XING:
            if z != line_zhi:
                relations.append(z)
    return list(set(relations))


