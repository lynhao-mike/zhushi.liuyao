def get_star_spirits(day_gan, day_zhi, month_zhi):
    """计算13个常用星煞对应的地支表.

    Returns:
        dict: {星煞名: 地支(或地支元组)}
    """
    day_group = _day_zhi_group(day_zhi)

    # 月支 -> 季节
    month_idx = DI_ZHI.index(month_zhi)
    # 寅卯辰=春, 巳午未=夏, 申酉戌=秋, 亥子丑=冬
    season_map = {
        "寅": "春", "卯": "春", "辰": "春",
        "巳": "夏", "午": "夏", "未": "夏",
        "申": "秋", "酉": "秋", "戌": "秋",
        "亥": "冬", "子": "冬", "丑": "冬",
    }
    season = season_map.get(month_zhi, "春")

    # 天医: 月支前一位
    tian_yi_idx = (month_idx - 1) % 12
    tian_yi = DI_ZHI[tian_yi_idx]

    return {
        "贵人": GUI_REN_TABLE.get(day_gan, ()),
        "禄神": LU_SHEN_TABLE.get(day_gan, ""),
        "羊刃": YANG_REN_TABLE.get(day_gan, ""),
        "文昌": WEN_CHANG_TABLE.get(day_gan, ""),
        "驿马": YI_MA_TABLE.get(day_group, "") if day_group else "",
        "桃花": TAO_HUA_TABLE.get(day_group, "") if day_group else "",
        "将星": JIANG_XING_TABLE.get(day_group, "") if day_group else "",
        "劫煞": JIE_SHA_TABLE.get(day_group, "") if day_group else "",
        "华盖": HUA_GAI_TABLE.get(day_group, "") if day_group else "",
        "谋星": MOU_XING_TABLE.get(day_group, "") if day_group else "",
        "灾煞": ZAI_SHA_TABLE.get(day_group, "") if day_group else "",
        "天医": tian_yi,
        "天喜": TIAN_XI_BY_SEASON.get(season, ""),
    }
